package com.chinasofti.ordersys.servlets.admin;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import com.chinasofti.ordersys.service.waiters.OrderService;

/**
 * <p>
 * Title: ChangeOrderStateServlet
 * </p>
 * <p>
 * Description: 修改订单状态信息的Servlet
 * </p>
 * <p>
 * Copyright: Copyright (c) 2015
 * </p>
 * <p>
 * Company: ChinaSoft International Ltd.
 * </p>
 * 
 * @author etc
 * @version 1.0
 */
public class ChangeOrderStateServlet extends HttpServlet {

	/**
	 * 当以GET方式请求Servlet时由service方法回调,本Servlet不同方法不要求不同的响应，因此在本Servlet中直接调用doPost
	 * ，以保证响应的一致性
	 * 
	 * @param request
	 *            请求对象
	 * @param response
	 *            响应对象
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		doPost(request, response);
	}

	/**
	 * 当以Post方式请求Servlet时由service方法回调
	 * 
	 * @param request
	 *            请求对象
	 * @param response
	 *            响应对象
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		// 获取界面传递的订单Id
		Integer orderId = new Integer(request.getParameter("orderId"));
		// 获取希望设定的目标订单状态值
		int state = Integer.parseInt(request.getParameter("state"));

		// System.out.println(orderId + "           " + state);
		// 创建订单管理服务对象
		OrderService service = new OrderService();
		// 修改订单的状态值
		service.changeState(orderId, state);
	}

}

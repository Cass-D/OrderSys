package com.chinasofti.ordersys.servlets.login;

import java.io.IOException;
import java.io.PrintWriter;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

import com.chinasofti.web.servlets.common.SaveCodeServlet;

/**
 * <p>
 * Title:CheckSaveCodeServlet
 * </p>
 * <p>
 * Description: 利用Ajax检查验证码是否输入正确的Servlet
 * </p>
 * <p>
 * Copyright: Copyright (c) 2015
 * </p>
 * <p>
 * Company: ChinaSoft International Ltd.
 * </p>
 * 
 * @author etc
 * @version 1.0
 */
public class CheckSaveCodeServlet extends HttpServlet {

	/**
	 * 当以GET方式请求Servlet时由service方法回调,本Servlet不同方法不要求不同的响应，因此在本Servlet中直接调用doPost
	 * ，以保证响应的一致性
	 * 
	 * @param request
	 *            请求对象
	 * @param response
	 *            响应对象
	 */
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {

		doPost(request, response);
	}

	/**
	 * 当以Post方式请求Servlet时由service方法回调
	 * 
	 * @param request
	 *            请求对象
	 * @param response
	 *            响应对象
	 */
	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		// 获取用户输入的验证码
		String inputCode = request.getParameter("code");
		// 获取会话对象
		HttpSession session = request.getSession();
		// 获取会话中保存的验证码
		String sessionCode = session.getAttribute(
				SaveCodeServlet.CODE_SESSION_ATTR_NAME).toString();

		// System.out.println(inputCode + "                   " + sessionCode);
		// 设置返回MIME类型
		response.setContentType("text/html");
		// 获取针对客户端的文本输出流
		PrintWriter out = response.getWriter();
		// 如果在忽略大小写的情况下用户输入的验证码与会话信息中保存的验证码能够匹配
		if (sessionCode.equalsIgnoreCase(inputCode)) {
			// 输出验证码正确的标识
			out.print("OK");
			// 如果不能匹配
		} else {
			// 输出验证码错误的标识
			out.print("FAIL");
		}

		// 刷新输出流
		out.flush();
		// 关闭输出流
		out.close();
	}

	

}
